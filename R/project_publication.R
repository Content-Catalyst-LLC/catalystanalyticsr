.catalyst_publication_schema_version <- function() "1.0.0"

.project_escape_html <- function(x) {
  x <- gsub("&", "&amp;", x, fixed = TRUE)
  x <- gsub("<", "&lt;", x, fixed = TRUE)
  x <- gsub(">", "&gt;", x, fixed = TRUE)
  x <- gsub('"', "&quot;", x, fixed = TRUE)
  x
}

.project_run_index <- function(project) {
  if (!length(project$runs)) return(data.frame(run_id = character(), label = character(), status = character(), scenario_ids = character(), model_id = character(), model_version = character(), input_hash = character(), output_hash = character(), review_status = character(), created_at = character(), stringsAsFactors = FALSE))
  do.call(rbind, lapply(project$runs, function(run) {
    model_id <- if (is.list(run$model)) run$model$id else if (is.null(run$model)) "" else as.character(run$model)[1L]
    model_version <- if (is.list(run$model)) run$model$version else ""
    data.frame(
      run_id = run$id, label = run$label, status = run$status, scenario_ids = paste(run$scenario_ids, collapse = ","),
      model_id = if (is.null(model_id)) "" else model_id, model_version = if (is.null(model_version)) "" else model_version,
      input_hash = run$input_hash, output_hash = run$output_hash, review_status = run$review_status, created_at = run$created_at, stringsAsFactors = FALSE
    )
  }))
}

.project_scenario_index <- function(project) {
  if (!length(project$scenarios)) return(data.frame(scenario_id = character(), title = character(), role = character(), model_id = character(), model_version = character(), fingerprint = character(), stringsAsFactors = FALSE))
  do.call(rbind, lapply(project$scenarios, function(scenario) data.frame(
    scenario_id = scenario$id, title = scenario$title, role = scenario$role, model_id = scenario$model$id,
    model_version = scenario$model$version, fingerprint = scenario_fingerprint(scenario), stringsAsFactors = FALSE
  )))
}

.project_dataset_index <- function(project) {
  if (!length(project$datasets)) return(data.frame(dataset_id = character(), title = character(), rows = integer(), columns = integer(), fingerprint = character(), license = character(), stringsAsFactors = FALSE))
  do.call(rbind, lapply(project$datasets, function(dataset) data.frame(
    dataset_id = dataset$id, title = dataset$title, rows = nrow(dataset$data), columns = ncol(dataset$data), fingerprint = dataset_fingerprint(dataset),
    license = if (is.null(dataset$source$license)) "" else dataset$source$license, stringsAsFactors = FALSE
  )))
}

.project_markdown <- function(project) {
  summary <- project_summary(project)
  run_lines <- if (!length(project$runs)) "- No runs recorded." else paste(vapply(project$runs, function(run) sprintf("- **%s** (`%s`): %s; input `%s`; output `%s`; review `%s`", run$label, run$id, run$status, run$input_hash, run$output_hash, run$review_status), character(1)), collapse = "\n")
  scenario_lines <- if (!length(project$scenarios)) "- No scenarios recorded." else paste(vapply(project$scenarios, function(scenario) sprintf("- **%s** (`%s`): %s using `%s@%s`", scenario$title, scenario$id, scenario$role, scenario$model$id, scenario$model$version), character(1)), collapse = "\n")
  note_lines <- if (!length(project$notes)) "- No interpretation notes recorded." else paste(vapply(project$notes, function(note) sprintf("- **%s** (%s): %s", note$id, note$status, note$text), character(1)), collapse = "\n")
  review_lines <- if (!length(project$reviews)) "- No review records." else paste(vapply(project$reviews, function(review) sprintf("- **%s** by %s: %s. %s", review$id, review$reviewer, review$decision, review$comments), character(1)), collapse = "\n")
  c(
    paste0("# ", project$title), "",
    paste0("**Project ID:** `", project$id, "`  "),
    paste0("**Owner:** ", ifelse(nzchar(project$owner), project$owner, "Not specified"), "  "),
    paste0("**Project fingerprint:** `", summary$fingerprint, "`  "),
    paste0("**Review status:** ", summary$review_status, "  "),
    paste0("**Generated by:** Catalyst Analytics R ", .catalyst_package_version()), "",
    "## Analytical purpose", ifelse(nzchar(project$description), project$description, "No description recorded."), "",
    "## Reproducibility record",
    paste0("- Scenarios: ", summary$scenarios), paste0("- Datasets: ", summary$datasets), paste0("- Models: ", summary$models), paste0("- Parameter sets: ", summary$parameter_sets),
    paste0("- Runs: ", summary$completed_runs, " completed; ", summary$failed_runs, " failed"), paste0("- Snapshots: ", summary$snapshots), "",
    "## Scenarios", scenario_lines, "", "## Analytical runs", run_lines, "", "## Interpretation notes", note_lines, "", "## Review record", review_lines, "",
    "## Method and use boundary",
    "The project preserves software, model, data, assumption, run, and review records. Reproducibility does not by itself establish external validity, causal identification, regulatory compliance, or fitness for a professional decision. Those judgments require qualified human review."
  )
}

.project_html <- function(project) {
  markdown <- .project_markdown(project)
  body <- paste(vapply(markdown, function(line) {
    escaped <- .project_escape_html(line)
    if (grepl("^# ", line)) return(paste0("<h1>", sub("^# ", "", escaped), "</h1>"))
    if (grepl("^## ", line)) return(paste0("<h2>", sub("^## ", "", escaped), "</h2>"))
    if (grepl("^- ", line)) return(paste0("<li>", sub("^- ", "", escaped), "</li>"))
    if (!nzchar(line)) return("")
    paste0("<p>", escaped, "</p>")
  }, character(1)), collapse = "\n")
  c(
    "<!doctype html>", '<html lang="en"><head><meta charset="utf-8">',
    '<meta name="viewport" content="width=device-width,initial-scale=1">',
    paste0("<title>", .project_escape_html(project$title), "</title>"),
    "<style>body{max-width:920px;margin:40px auto;padding:0 24px;font:16px/1.65 system-ui,sans-serif;color:#171614}h1,h2{line-height:1.2}h2{margin-top:2rem;border-bottom:1px solid #ddd;padding-bottom:.35rem}li{margin:.35rem 0}code{background:#f4f1ed;padding:.1rem .3rem;border-radius:4px}</style></head><body>", body, "</body></html>"
  )
}

#' Build a Decision Studio handoff
#'
#' @param project A project.
#' @return Governed Decision Studio handoff record.
#' @export
decision_studio_handoff <- function(project) {
  validate_catalyst_project(project)
  alternatives <- lapply(project$scenarios, function(scenario) list(id = scenario$id, title = scenario$title, role = scenario$role, model = scenario$model, assumptions = scenario$assumptions, constraints = scenario$constraints))
  analytical_evidence <- lapply(project$runs, function(run) list(id = run$id, label = run$label, status = run$status, scenario_ids = run$scenario_ids, result_summary = run$result_summary, input_hash = run$input_hash, output_hash = run$output_hash, warnings = run$warnings, errors = run$errors, review_status = run$review_status))
  decision_boundary <- list(human_decision_required = TRUE, uncertainty_must_be_disclosed = TRUE, limitations_must_be_disclosed = TRUE, approval_not_inferred_from_reproducibility = TRUE)
  result <- .platform_common(project, "decision_studio", "decision_studio_analytical_project", project$title,
    list(question = project$description, scope = project$scope, alternatives = alternatives, analytical_evidence = analytical_evidence, interpretations = project$notes, reviews = project$reviews),
    c(decision_boundary, list(human_review_required = TRUE)),
    review = list(status = project$metadata$review_status, human_reviewer_required = TRUE))
  result$question <- project$description; result$scope <- project$scope; result$alternatives <- alternatives
  result$analytical_evidence <- analytical_evidence; result$interpretations <- project$notes; result$reviews <- project$reviews
  result$decision_boundary <- decision_boundary
  result
}

#' Build a Knowledge Library methodology handoff
#'
#' @param project A project.
#' @return Methodology and evidence package.
#' @export
knowledge_library_handoff <- function(project) {
  validate_catalyst_project(project)
  sources <- list()
  for (dataset in project$datasets) sources[[length(sources) + 1L]] <- dataset$source
  assumptions <- unlist(lapply(project$scenarios, function(scenario) scenario$assumptions), recursive = FALSE)
  runs <- lapply(project$runs, function(run) run[c("id", "label", "status", "model", "input_hash", "output_hash", "result_summary", "warnings", "errors", "review_status")])
  reproducibility <- list(environment = project$environment, snapshots = project$snapshots, package_version = .catalyst_package_version())
  publication_boundary <- list(methodology_record_not_peer_review = TRUE, sources_require_citation_review = TRUE, limitations_require_prominent_disclosure = TRUE)
  title <- paste0(project$title, " - Methodology and Reproducibility Record")
  result <- .platform_common(project, "knowledge_library", "knowledge_library_methodology_package", title,
    list(abstract = project$description, scope = project$scope, models = project$models, datasets = lapply(project$datasets, dataset_manifest), sources = sources, assumptions = assumptions, parameter_sets = project$parameter_sets, runs = runs, interpretations = project$notes, review_record = project$reviews, reproducibility = reproducibility),
    c(publication_boundary, list(human_review_required = TRUE)),
    review = list(status = project$metadata$review_status, human_reviewer_required = TRUE))
  result$abstract <- project$description; result$scope <- project$scope; result$models <- project$models
  result$datasets <- lapply(project$datasets, dataset_manifest); result$sources <- sources; result$assumptions <- assumptions
  result$parameter_sets <- project$parameter_sets; result$runs <- runs; result$interpretations <- project$notes
  result$review_record <- project$reviews; result$reproducibility <- reproducibility; result$publication_boundary <- publication_boundary
  result
}

.project_write_json <- function(value, path) {
  jsonlite::write_json(.safe_json_value(value), path, auto_unbox = TRUE, pretty = TRUE, null = "null", na = "null", digits = NA, dataframe = "rows")
  path
}

.project_copy_plots <- function(project, bundle_dir) {
  if (!length(project$plots)) return(list())
  plot_dir <- file.path(bundle_dir, "figures"); dir.create(plot_dir, recursive = TRUE, showWarnings = FALSE)
  lapply(project$plots, function(plot) {
    destination <- file.path(plot_dir, paste0(plot$id, ".", tools::file_ext(plot$file)))
    copied <- file.copy(plot$source_path, destination, overwrite = TRUE)
    if (!copied) stop(sprintf("Could not copy plot `%s`.", plot$id), call. = FALSE)
    list(id = plot$id, file = file.path("figures", basename(destination)), title = plot$title, caption = plot$caption, bytes = unname(file.info(destination)$size), md5 = unname(tools::md5sum(destination)))
  })
}

#' Export a reproducible project and analytical publication bundle
#'
#' @param project A `catalyst_project`.
#' @param dir Parent output directory.
#' @param prefix Bundle directory name.
#' @param formats Publication formats: `json`, `csv`, `markdown`, `html`, and `quarto`.
#' @param include_results Include complete run result payloads.
#' @param include_plots Copy registered plot files.
#' @param zip_bundle Create a ZIP archive.
#' @return Named paths to generated artifacts.
#' @export
export_project_publication <- function(project, dir = ".", prefix = "catalyst-analytical-project", formats = c("json", "csv", "markdown", "html", "quarto"), include_results = TRUE, include_plots = TRUE, zip_bundle = TRUE) {
  validate_catalyst_project(project); .assert_single_string(dir, "dir"); .assert_single_string(prefix, "prefix"); .assert_flag(include_results, "include_results"); .assert_flag(include_plots, "include_plots"); .assert_flag(zip_bundle, "zip_bundle")
  formats <- unique(tolower(formats)); allowed <- c("json", "csv", "markdown", "html", "quarto")
  if (!length(formats) || any(!formats %in% allowed)) stop("`formats` contains an unsupported publication format.", call. = FALSE)
  bundle_dir <- file.path(dir, prefix); dir.create(bundle_dir, recursive = TRUE, showWarnings = FALSE)
  paths <- list(bundle_dir = bundle_dir)
  export_project <- project
  if (!include_results) export_project$runs <- lapply(export_project$runs, function(run) { run$result <- NULL; run })
  if ("json" %in% formats) {
    paths$project <- .project_write_json(unclass(export_project), file.path(bundle_dir, "project.json"))
    paths$project_manifest <- .project_write_json(project_manifest(project), file.path(bundle_dir, "project-manifest.json"))
    paths$environment <- .project_write_json(project$environment, file.path(bundle_dir, "environment.json"))
  }
  if ("csv" %in% formats) {
    utils::write.csv(.project_run_index(project), file.path(bundle_dir, "run-index.csv"), row.names = FALSE); paths$run_index <- file.path(bundle_dir, "run-index.csv")
    utils::write.csv(.project_scenario_index(project), file.path(bundle_dir, "scenario-index.csv"), row.names = FALSE); paths$scenario_index <- file.path(bundle_dir, "scenario-index.csv")
    utils::write.csv(.project_dataset_index(project), file.path(bundle_dir, "dataset-index.csv"), row.names = FALSE); paths$dataset_index <- file.path(bundle_dir, "dataset-index.csv")
  }
  markdown <- .project_markdown(project)
  if ("markdown" %in% formats) { paths$markdown <- file.path(bundle_dir, "analytical-publication.md"); writeLines(markdown, paths$markdown, useBytes = TRUE) }
  if ("html" %in% formats) { paths$html <- file.path(bundle_dir, "analytical-publication.html"); writeLines(.project_html(project), paths$html, useBytes = TRUE) }
  if ("quarto" %in% formats) {
    paths$quarto <- file.path(bundle_dir, "analytical-publication.qmd")
    front <- c("---", paste0('title: "', gsub('"', "'", project$title, fixed = TRUE), '"'), "format:", "  html:", "    toc: true", "execute:", "  enabled: false", "---", "")
    writeLines(c(front, markdown[-1L]), paths$quarto, useBytes = TRUE)
  }
  paths$decision_studio <- .project_write_json(decision_studio_handoff(project), file.path(bundle_dir, "decision-studio-handoff.json"))
  paths$knowledge_library <- .project_write_json(knowledge_library_handoff(project), file.path(bundle_dir, "knowledge-library-methodology.json"))
  copied_plots <- if (include_plots) .project_copy_plots(project, bundle_dir) else list()
  files <- list.files(bundle_dir, recursive = TRUE, full.names = TRUE, all.files = FALSE)
  normalized_bundle <- normalizePath(bundle_dir)
  file_records <- lapply(files, function(path) {
    normalized <- normalizePath(path)
    relative <- substring(normalized, nchar(normalized_bundle) + 2L)
    list(file = relative, bytes = unname(file.info(path)$size), md5 = unname(tools::md5sum(path)))
  })
  manifest <- list(
    schema_version = .catalyst_publication_schema_version(), export_type = "reproducible_analytical_project_publication",
    project_id = project$id, project_fingerprint = project_fingerprint(project), package = list(name = "catalystanalyticsr", version = .catalyst_package_version()),
    created_at = .utc_now(), formats = formats, include_results = include_results, plots = copied_plots,
    review_status = project$metadata$review_status, publication_status = project$metadata$publication_status,
    file_count = length(file_records), files = file_records, integrity = list(hash_algorithm = "md5", complete = TRUE, scope = "all_bundle_files_except_manifest"),
    use_boundary = list(reproducible_not_necessarily_valid = TRUE, review_required = TRUE, not_professional_advice = TRUE)
  )
  paths$manifest <- .project_write_json(manifest, file.path(bundle_dir, "manifest.json"))
  if (zip_bundle) {
    zip_path <- file.path(dir, paste0(prefix, ".zip")); old <- getwd(); on.exit(setwd(old), add = TRUE); setwd(dir)
    utils::zip(zipfile = basename(zip_path), files = prefix); paths$zip <- zip_path
  }
  paths
}
